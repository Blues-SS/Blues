CREATE TABLE SPRINT (
  ID_SPRINT SERIAL PRIMARY KEY,
  NOME VARCHAR(200),
  STATUS VARCHAR(200),
  DT_INICIO TIMESTAMP,
  DT_FIM TIMESTAMP,
  DT_CRIACAO TIMESTAMP,
  DT_ALTERACAO TIMESTAMP
);

CREATE TABLE HISTORIA (
  ID_HISTORIA SERIAL PRIMARY KEY,
  ID_SPRINT INTEGER ,
  STATUS VARCHAR(50),
  NOME VARCHAR(20),
  DESCRICAO VARCHAR(600),
  VALUE_BUSINESS INTEGER,
  PONTOS INTEGER,
  DT_CRIACAO TIMESTAMP,
  DT_ALTERACAO TIMESTAMP
);


ALTER TABLE HISTORIA ADD CONSTRAINT FK_SPRINT FOREIGN KEY (ID_SPRINT) REFERENCES SPRINT (ID_SPRINT) MATCH FULL;
ALTER SEQUENCE historia_id_historia_seq RESTART WITH 1000;


